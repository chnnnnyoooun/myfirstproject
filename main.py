import streamlit as st

st.title("오늘 기분에 맞는 저녁 메뉴 추천")

# 기분 선택
mood = st.selectbox(
    "오늘 당신의 기분은 어떤가요?",
    ["행복해요", "우울해요", "피곤해요", "신나요", "쓸쓸해요", "화나요"]
)

# 기분별 저녁 메뉴 추천
menu_recommendations = {
    "행복해요": "기분 좋은 날엔 🍣 스시 어때요?",
    "우울해요": "든든한 🍜 라멘 한 그릇으로 기분 전환해요.",
    "피곤해요": "따뜻한 🍲 삼계탕이 회복에 좋아요!",
    "신나요": "에너지 넘치는 🍗 양념 치킨 추천!",
    "쓸쓸해요": "따뜻한 🍛 카레와 함께 혼밥도 괜찮아요.",
    "화나요": "매운 🌶️ 떡볶이로 스트레스를 날려봐요!"
}

# 추천 결과 출력
if mood:
    st.subheader("추천 저녁 메뉴:")
    st.write(menu_recommendations.get(mood, "추천 메뉴를 찾을 수 없어요."))
